public class Course {
	private String cCode;
	private String cName;
	private String cInstitution;
	private String cManager;
	private String cPoints;

	public String getcCode() {
		return cCode;
	}

	public void setcCode(String cCode) {
		this.cCode = cCode;
	}

	public String getcName() {
		return cName;
	}

	public void setcName(String cName) {
		this.cName = cName;
	}

	public String getcInstitution() {
		return cInstitution;
	}

	public void setcInstitution(String cInstitution) {
		this.cInstitution = cInstitution;
	}

	public String getcManager() {
		return cManager;
	}

	public void setcManager(String cManager) {
		this.cManager = cManager;
	}

	public String getcPoints() {
		return cPoints;
	}

	public void setcPoints(String cPoints) {
		this.cPoints = cPoints;
	}

}
